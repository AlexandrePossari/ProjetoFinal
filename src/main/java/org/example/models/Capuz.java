package org.example.models;

public class Capuz {
    private String data;
    private Couro couro;
    private int preco;
    private int lucro;

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public Couro getCouro() {
        return couro;
    }

    public void setCouro(Couro couro) {
        this.couro = couro;
    }

    public int getPreco() {
        return preco;
    }

    public void setPreco(int preco) {
        this.preco = preco;
    }

    public int getLucro() {
        return lucro;
    }

    public void setLucro(int lucro) {
        this.lucro = lucro;
    }
}
